# for loop printing the num from 1 to 10
for i in range(1,11):
    print(i)

# if we give one argument in range like range(100) then it will print 0 to 99
# if we give two argument in range like range(2,11) then it will print 2 to 10.
# if we give 3 arguments in range like range(1,11,2) then 3rd parameter will do i=i+2 .